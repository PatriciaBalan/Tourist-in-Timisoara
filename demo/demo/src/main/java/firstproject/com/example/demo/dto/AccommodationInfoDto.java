package firstproject.com.example.demo.dto;

public class AccommodationInfoDto {

    private int accommodationId;

    private String accommodationName;

    private String accommodationDetail;

    public int getAccommodationId() {
        return accommodationId;
    }

    public void setAccommodationId(int accommodationId) {
        this.accommodationId = accommodationId;
    }

    public String getAccommodationName() {
        return accommodationName;
    }

    public void setAccommodationName(String accommodationName) {
        this.accommodationName = accommodationName;
    }

    public String getAccommodationDetail() {
        return accommodationDetail;
    }

    public void setAccommodationDetail(String accommodationDetail) {
        this.accommodationDetail = accommodationDetail;
    }
}
